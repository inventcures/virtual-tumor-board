"use client";

import { useState, useEffect, useCallback } from "react";
import { 
  X, 
  Download, 
  Share2, 
  FileText,
  Loader2,
  CheckCircle,
  MessageCircle,
  RefreshCw,
  BookOpen,
  GraduationCap,
  Stethoscope,
  Info
} from "lucide-react";
import { 
  generateEnhancedPDFReport, 
  downloadPDF,
  LITERACY_CONFIGS,
  type MedicalLiteracyLevel 
} from "@/lib/pdf-report-v2";

interface AgentResponse {
  response: string;
  citations: string[];
  toolsUsed: string[];
}

interface PDFPreviewModalV2Props {
  isOpen: boolean;
  onClose: () => void;
  consensus: string;
  agentResponses: Record<string, AgentResponse>;
  caseInfo: {
    cancerSite?: string;
    stage?: string;
    tnm?: string;
    documentCount?: number;
  };
}


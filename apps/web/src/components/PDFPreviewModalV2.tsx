// Literacy level icons
const LITERACY_ICONS: Record<MedicalLiteracyLevel, typeof BookOpen> = {
  simple: BookOpen,
  standard: GraduationCap,
  technical: Stethoscope,
};

// Literacy level colors
const LITERACY_COLORS: Record<MedicalLiteracyLevel, string> = {
  simple: 'from-emerald-500 to-teal-600',
  standard: 'from-blue-500 to-indigo-600',
  technical: 'from-purple-500 to-pink-600',
};

export function PDFPreviewModalV2({ 
  isOpen, 
  onClose, 
  consensus,
  agentResponses,
  caseInfo 
}: PDFPreviewModalV2Props) {
  const [literacyLevel, setLiteracyLevel] = useState<MedicalLiteracyLevel>('standard');
  const [pdfBlob, setPdfBlob] = useState<Blob | null>(null);
  const [pdfUrl, setPdfUrl] = useState<string | null>(null);
  const [isGenerating, setIsGenerating] = useState(false);
  const [isSharing, setIsSharing] = useState(false);
  const [shareSuccess, setShareSuccess] = useState(false);

  // Generate PDF when modal opens or literacy level changes
  const generatePDF = useCallback(async () => {
    if (!consensus) return;
    
    setIsGenerating(true);
    
    try {
      const blob = await generateEnhancedPDFReport(
        consensus,
        agentResponses,
        caseInfo,
        literacyLevel
      );
      setPdfBlob(blob);
      
      // Create URL for preview
      if (pdfUrl) {
        URL.revokeObjectURL(pdfUrl);
      }
      const url = URL.createObjectURL(blob);
      setPdfUrl(url);
    } catch (error) {
      console.error('PDF generation error:', error);
    } finally {
      setIsGenerating(false);
    }
  }, [consensus, agentResponses, caseInfo, literacyLevel, pdfUrl]);

  // Generate on open and literacy change
  useEffect(() => {
    if (isOpen && consensus) {
      generatePDF();
    }
  }, [isOpen, literacyLevel, consensus]);

  // Cleanup URL on close
  useEffect(() => {
    return () => {
      if (pdfUrl) {
        URL.revokeObjectURL(pdfUrl);
      }
    };
  }, [pdfUrl]);

  if (!isOpen) return null;

  const handleDownload = () => {
    if (!pdfBlob) return;
    const filename = `tumor-board-report-${literacyLevel}-${new Date().toISOString().split('T')[0]}.pdf`;
    downloadPDF(pdfBlob, filename);
  };

  const handleWhatsAppShare = async () => {
    setIsSharing(true);
    
    try {
      const levelLabel = LITERACY_CONFIGS[literacyLevel].label;
      const message = encodeURIComponent(
        `Virtual Tumor Board Report\n\n` +
        `Cancer: ${caseInfo.cancerSite || 'Not specified'}\n` +
        `Stage: ${caseInfo.stage || 'Not determined'}\n` +
        `Report Type: ${levelLabel}\n\n` +
        `This AI-generated report contains multi-specialist oncology recommendations. ` +
        `Please review with your treating oncologist.\n\n` +
        `Generated by Virtual Tumor Board\n` +
        `https://virtual-tumor-board-production.up.railway.app`
      );
      
      // Try Web Share API first
      if (navigator.share && pdfBlob) {
        const file = new File([pdfBlob], 'tumor-board-report.pdf', { type: 'application/pdf' });
        
        if (navigator.canShare && navigator.canShare({ files: [file] })) {
          await navigator.share({
            files: [file],
            title: 'Tumor Board Report',
            text: `Tumor Board Report (${levelLabel}) for ${caseInfo.cancerSite || 'Cancer'} case`,
          });
          setShareSuccess(true);
          setTimeout(() => setShareSuccess(false), 3000);
          return;
        }
      }
      
      // Fallback to WhatsApp URL
      window.open(`https://wa.me/?text=${message}`, '_blank');
      setShareSuccess(true);
      setTimeout(() => setShareSuccess(false), 3000);
      
    } catch (error) {
      console.error('Share error:', error);
      const message = encodeURIComponent(
        `Virtual Tumor Board Report for ${caseInfo.cancerSite || 'Cancer'}\n\n` +
        `View at: https://virtual-tumor-board-production.up.railway.app`
      );
      window.open(`https://wa.me/?text=${message}`, '_blank');
    } finally {
      setIsSharing(false);
    }
  };

  const handleNativeShare = async () => {
    if (!navigator.share) {
      handleWhatsAppShare();
      return;
    }

    try {
      if (pdfBlob) {
        const file = new File([pdfBlob], 'tumor-board-report.pdf', { type: 'application/pdf' });
        
        if (navigator.canShare && navigator.canShare({ files: [file] })) {
          await navigator.share({
            files: [file],
            title: 'Tumor Board Report',
            text: `Virtual Tumor Board Report for ${caseInfo.cancerSite || 'Cancer'}`,
          });
          return;
        }
      }
      
      await navigator.share({
        title: 'Tumor Board Report',
        text: `Virtual Tumor Board Report for ${caseInfo.cancerSite || 'Cancer'}`,
        url: window.location.href,
      });
    } catch (error) {
      console.log('Share cancelled or failed');
    }
  };

  const config = LITERACY_CONFIGS[literacyLevel];
  const LiteracyIcon = LITERACY_ICONS[literacyLevel];

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-black/80 backdrop-blur-sm">
      <div className="bg-slate-900 rounded-2xl border border-slate-700 w-full max-w-5xl max-h-[95vh] flex flex-col overflow-hidden shadow-2xl">
        {/* Header */}
        <div className="flex items-center justify-between px-6 py-4 border-b border-slate-700 bg-gradient-to-r from-indigo-600/20 to-purple-600/20">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 rounded-lg bg-gradient-to-br from-indigo-500 to-purple-600 flex items-center justify-center">
              <FileText className="w-5 h-5 text-white" />
            </div>
            <div>
              <h2 className="text-lg font-semibold text-white">Tumor Board Report</h2>
              <p className="text-sm text-slate-400">
                {caseInfo.cancerSite || 'Cancer'} - {caseInfo.stage || 'Stage TBD'}
              </p>
            </div>
          </div>
          <button
            onClick={onClose}
            className="p-2 rounded-lg hover:bg-slate-800 transition-colors"
          >
            <X className="w-5 h-5 text-slate-400" />
          </button>
        </div>

        {/* Literacy Level Selector */}
        <div className="px-6 py-4 border-b border-slate-700/50 bg-slate-800/30">
          <div className="flex items-center gap-2 mb-3">
            <Info className="w-4 h-4 text-indigo-400" />
            <span className="text-sm font-medium text-slate-300">
              Choose report complexity for your needs:
            </span>
          </div>
          
          <div className="grid grid-cols-3 gap-3">
            {(Object.keys(LITERACY_CONFIGS) as MedicalLiteracyLevel[]).map((level) => {
              const levelConfig = LITERACY_CONFIGS[level];
              const Icon = LITERACY_ICONS[level];
              const isSelected = literacyLevel === level;
              
              return (
                <button
                  key={level}
                  onClick={() => setLiteracyLevel(level)}
                  disabled={isGenerating}
                  className={`relative p-4 rounded-xl border-2 transition-all text-left ${
                    isSelected 
                      ? `border-indigo-500 bg-indigo-500/10` 
                      : 'border-slate-700 hover:border-slate-600 bg-slate-800/50 hover:bg-slate-800'
                  } ${isGenerating ? 'opacity-50 cursor-not-allowed' : ''}`}
                >
                  {/* Selection indicator */}
                  {isSelected && (
                    <div className="absolute top-2 right-2">
                      <CheckCircle className="w-5 h-5 text-indigo-400" />
                    </div>
                  )}
                  
                  {/* Icon */}
                  <div className={`w-10 h-10 rounded-lg bg-gradient-to-br ${LITERACY_COLORS[level]} flex items-center justify-center mb-2`}>
                    <Icon className="w-5 h-5 text-white" />
                  </div>
                  
                  {/* Label */}
                  <h4 className={`font-semibold mb-1 ${isSelected ? 'text-white' : 'text-slate-300'}`}>
                    {levelConfig.label}
                  </h4>
                  
                  {/* Description */}
                  <p className="text-xs text-slate-400 line-clamp-2">
                    {levelConfig.description}
                  </p>
                </button>
              );
            })}
          </div>
          
          {/* Current selection indicator */}
          <div className="mt-3 flex items-center gap-2 text-sm">
            <span className="text-slate-500">Selected:</span>
            <span className={`px-2 py-0.5 rounded-full bg-gradient-to-r ${LITERACY_COLORS[literacyLevel]} text-white text-xs font-medium`}>
              {config.label}
            </span>
            {isGenerating && (
              <span className="flex items-center gap-1 text-indigo-400">
                <RefreshCw className="w-3 h-3 animate-spin" />
                Updating preview...
              </span>
            )}
          </div>
        </div>

        {/* PDF Preview */}
        <div className="flex-1 overflow-hidden bg-slate-950 relative">
          {isGenerating && (
            <div className="absolute inset-0 bg-slate-950/80 flex items-center justify-center z-10">
              <div className="flex flex-col items-center gap-3">
                <Loader2 className="w-10 h-10 text-indigo-400 animate-spin" />
                <span className="text-slate-300 text-sm">Generating {config.label} report...</span>
              </div>
            </div>
          )}
          
          {pdfUrl ? (
            <iframe
              src={`${pdfUrl}#toolbar=0&view=FitH`}
              className="w-full h-full min-h-[350px]"
              title="PDF Preview"
            />
          ) : (
            <div className="flex items-center justify-center h-full min-h-[350px]">
              <Loader2 className="w-8 h-8 text-indigo-400 animate-spin" />
            </div>
          )}
        </div>

        {/* Action Buttons */}
        <div className="px-6 py-4 border-t border-slate-700 bg-slate-900/50">
          <div className="flex flex-col sm:flex-row items-stretch sm:items-center gap-3">
            {/* Download Button */}
            <button
              onClick={handleDownload}
              disabled={!pdfBlob || isGenerating}
              className="flex-1 flex items-center justify-center gap-2 px-6 py-3 bg-gradient-to-r from-indigo-600 to-purple-600 hover:from-indigo-500 hover:to-purple-500 text-white font-medium rounded-xl transition-all disabled:opacity-50 disabled:cursor-not-allowed"
            >
              <Download className="w-5 h-5" />
              Download {config.label} PDF
            </button>

            {/* WhatsApp Share Button */}
            <button
              onClick={handleWhatsAppShare}
              disabled={isSharing || !pdfBlob}
              className="flex-1 flex items-center justify-center gap-2 px-6 py-3 bg-[#25D366] hover:bg-[#20BD5A] text-white font-medium rounded-xl transition-all disabled:opacity-50"
            >
              {isSharing ? (
                <Loader2 className="w-5 h-5 animate-spin" />
              ) : shareSuccess ? (
                <CheckCircle className="w-5 h-5" />
              ) : (
                <WhatsAppIcon className="w-5 h-5" />
              )}
              {shareSuccess ? 'Shared!' : 'Share on WhatsApp'}
            </button>

            {/* Native Share Button */}
            <button
              onClick={handleNativeShare}
              disabled={!pdfBlob}
              className="sm:flex-none flex items-center justify-center gap-2 px-4 py-3 bg-slate-800 hover:bg-slate-700 text-slate-300 font-medium rounded-xl transition-all disabled:opacity-50"
            >
              <Share2 className="w-5 h-5" />
              <span className="sm:hidden">Share</span>
            </button>
          </div>

          {/* Hints */}
          <div className="mt-3 flex flex-wrap items-center justify-center gap-x-4 gap-y-1 text-xs text-slate-500">
            <span>Share with your doctor or family</span>
            <span className="hidden sm:inline">-</span>
            <span>Report adapts to selected reading level</span>
          </div>
        </div>
      </div>
    </div>
  );
}
